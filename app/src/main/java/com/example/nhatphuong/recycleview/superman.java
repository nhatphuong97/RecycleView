package com.example.nhatphuong.recycleview;

public class superman {
    public int Image;
    public String Name;

    public int getImage() {
        return Image;

    }

    public superman(int image, String name) {
        Image = image;
        Name = name;
    }

    public void setImage(int image) {
        Image = image;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
